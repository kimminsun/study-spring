package mybatisDept;

public class Dept1 {
	private int no;
	private String name;
	
	public Dept1()
	{
		super();
	}
	public Dept1(int no,String name)
	{
		super();
		this.no=no;
		this.name=name;
	}
	public int getNo() {
		return no;
	}
	public void setNo(int no) {
		this.no = no;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}

	
}
